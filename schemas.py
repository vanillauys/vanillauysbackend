# ---------------------------------------------------------------------------- #
# --- Imports ---------------------------------------------------------------- #
# ---------------------------------------------------------------------------- #


from pydantic import BaseModel, Field
from typing import Union


# ---------------------------------------------------------------------------- #
# --- General Schemas -------------------------------------------------------- #
# ---------------------------------------------------------------------------- #


class Message(BaseModel):
    message: str


# ---------------------------------------------------------------------------- #
# --- Caclulators Schemas ---------------------------------------------------- #
# ---------------------------------------------------------------------------- #


class Interest(BaseModel):
    total: float
    interest: float


class Age(BaseModel):
    seconds: float
    minutes: float
    hours: float
    days: float
    weeks: list[float]
    md: list[float]
    ymd: list[float]


# ---------------------------------------------------------------------------- #
# --- Crypto Schemas --------------------------------------------------------- #
# ---------------------------------------------------------------------------- #


class Crypto(BaseModel):
    name: str
    price: float
    change: float
